    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        try{
            Empleado e = new Empleado(Integer.parseInt(jtDocumento.getText()),
                jtNombre.getText(),
                comboCategoria.getSelectedItem().toString(),
                Double.parseDouble(jtSueldo.getText()),
                (Empresa)comboEmpresa.getSelectedItem());
                e.getEmpresa().agregarEmpleado(e);
                limpiar();
                listaEmpleados.add(e);
        }catch(Exception ex){
            
        }
        
    }//GEN-LAST:event_guardarActionPerformed
    private void limpiar(){
        jtDocumento.setText("");
        jtNombre.setText("");
        jtSueldo.setText("");
        jtApellido.setText("");
        
    }
    
    private void cargarModelo(){
        modelo.addColumn("Documento");
        modelo.addColumn("Nombre");
        modelo.addColumn("Sueldo");
        modelo.addColumn("Empresa");
        modelo.addColumn("Categoria");
        cargarATabla();
        
    }
    private void cargarATabla(){
        jtTabla.setModel(modelo);
    }
    
    
