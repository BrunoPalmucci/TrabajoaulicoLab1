    private void crearEmpresaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearEmpresaActionPerformed

        Empresa e = new Empresa(jtRazonSocial.getText(),
                Integer.parseInt(jtCUIT.getText()));
        if (!listaEmpresas.contains(e)){
            comboEmpresa.addItem(e);
            listaEmpresas.add(e);
            guardar.setEnabled(true);
            mostrarEmpleados.setEnabled(true);
        }else{
            JOptionPane.showMessageDialog(this, "Esta empresa ya existe", "Error", 0);
        }
        
        
    }//GEN-LAST:event_crearEmpresaActionPerformed

    private void mostrarEmpleadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarEmpleadosActionPerformed
        ((DefaultTableModel)jtTabla.getModel()).setNumRows(0);
        for (Empleado e:listaEmpleados){
            modelo.addRow(new Object[]{e.getDni(),
                e.getNombreApellido(),
                e.getSueldo(),
                e.getEmpresa().toString(),
                e.getCategoria()});
        }
        cargarATabla();
    }//GEN-LAST:event_mostrarEmpleadosActionPerformed

